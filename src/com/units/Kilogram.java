package com.units;

public class Kilogram extends UnitBase {

    public Kilogram() {
        super();
        this.setSymbol("kg");
    }

    public Kilogram(double i) {
        super(i);
        this.setSymbol("kg");
    }

    public Kilogram(double i, int j) {
        super(i, j);
        this.setSymbol("kg");
    }

}